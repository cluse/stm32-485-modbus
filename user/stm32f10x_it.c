/*
  USART1中断服务程序
*/


							  
void USART1_IRQHandler(void)
{
   char dat1;
   if(USART_GetITStatus(USART1,USART_IT_RXNE)!=RESET) //判断是否产生接收中断
   {
		 dat1=USART_ReceiveData(USART1);
		 USART_SendData(USART1,dat1); //当产生接收中断的时候,接收该数据，然后再从串口1把数据发送出去
		 while(USART_GetFlagStatus(USART1,USART_FLAG_TXE)==RESET);//等待发送完成（USART_FLAG_TXE:发送数据寄存器空标志位）
   } 
}
